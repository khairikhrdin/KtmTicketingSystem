    //Class to calculate ktm price
    public class ktmPrice
    {
        private String origin = null;
        private String destination = null;
        private String way = null;
        private String citizenC = null;
        private double price = 0.00;
        private int quantity;

        private int xLoc = 0;
        private int yLoc = 0;
        private int wLoc = 0;
        private int cit = 0;

        public ktmPrice(String OriginLoc, String DestiLoc, String WayLoc, String Citizen, int Quantity)
        {
            KTMTicketingv2.Controllers.ktmTable ktmTab = new KTMTicketingv2.Controllers.ktmTable();
            String way = "Return";
            String citizenC = "Normal Citizen";
            int quantity = Quantity;
            int xLoc = Int32.Parse(OriginLoc);
            int yLoc = Int32.Parse(DestiLoc);
            int wLoc = Int32.Parse(WayLoc);
            int cit = Int32.Parse(Citizen);
            String origin = ktmTab.dictLane[xLoc];
            String destination = ktmTab.dictLane[yLoc];
            double priceGo = ktmTab.rate[xLoc, yLoc];
            double priceRe = ktmTab.rate[yLoc, xLoc];
            double price = 0.00;
            DateTime date1 = new DateTime(2019, 1, 4, 0, 0, 0);
            DateTime date2 = DateTime.Now;
            int result = DateTime.Compare(date2, date1);
            //Check whether date already 1 April 2019 or not
            if (result >= 0)
            {
                priceGo = priceGo - (priceGo * 0.2);
                priceRe = priceRe - (priceRe * 0.2);
            }
            //Check type of citizen
            if (cit == 1)
            {
                priceGo = priceGo - (priceGo * 0.3);
                priceRe = priceRe - (priceRe * 0.3);
                citizenC = "Senior Citizen";
            }
            else if (cit == 2)
            {
                priceGo = priceGo - (priceGo * 0.3);
                priceRe = priceRe - (priceRe * 0.3);
                citizenC = "Disabled";
            }
            else if (cit == 3)
            {
                priceGo = priceGo - (priceGo * 0.5);
                priceRe = priceRe - (priceRe * 0.5);
                citizenC = "Student";
            }
            //Check whether user choose return or one way
            if (wLoc == 0)
            {
                way = "Return";
                price = (priceGo + priceRe) * quantity;
            }
            else
            {
                way = "One Way";
                price = priceGo * quantity;
            }
            this.quantity = quantity;
            this.xLoc = xLoc;
            this.yLoc = yLoc;
            this.wLoc = wLoc;
            this.cit = cit;
            this.origin = origin;
            this.destination = destination;
            this.way = way;
            this.citizenC = citizenC;
            this.price = price;
        }
        public int getQuantity()
        {
            return quantity;
        }
        public int getOriginID()
        {
            return xLoc;
        }
        public int getDestinationID()
        {
            return yLoc;
        }
        public int getWayID()
        {
            return wLoc;
        }
        public int getCitizenID()
        {
            return cit;
        }
        public String getOrigin()
        {
            return origin;
        }
        public String getDestination()
        {
            return destination;
        }
        public String getWay()
        {
            return way;
        }
        public String getCitizen()
        {
            return citizenC;
        }
        public double getPrice()
        {
            return price;
        }
    }

